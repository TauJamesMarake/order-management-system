-- 9. HELPER VIEW

CREATE OR REPLACE VIEW v_orders_with_creator AS
SELECT
  o.id,
  o.order_number,
  o.client_name,
  o.mineral_type,
  o.quantity_kg,
  o.unit_price_zar,
  o.total_zar,
  o.status,
  o.notes,
  o.created_at,
  o.updated_at,
  u.id        AS creator_id,
  u.full_name AS creator_name,
  u.email     AS creator_email
FROM orders o
JOIN users u ON o.created_by = u.id;

COMMENT ON VIEW v_orders_with_creator IS 'Orders joined with creator — used by API list and report queries.';


